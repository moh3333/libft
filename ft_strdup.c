#include "libft.h"

char	*ft_strdup(const char	*s1)
{
	int		i;
	char	*str;

	i = ft_strlen(s1);
	str = (char *) malloc ((i + 1) * sizeof(char));
	if (!str)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
